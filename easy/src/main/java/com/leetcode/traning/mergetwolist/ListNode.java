package com.leetcode.traning.mergetwolist;

/**
 * @author Nikolay Chechenko.
 * @since 14.05.2020
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
